<script>

  document.addEventListener("DOMContentLoaded", () => {
    const showMoreButtons = document.querySelectorAll(".show-more");

    showMoreButtons.forEach((button) => {
      button.addEventListener("click", () => {
        const allCards = document.querySelectorAll(".pricing-card");
        const allHiddenFeatures = document.querySelectorAll(".pricing-card .hidden");

        // Check if "Show More" or "Show Less"
        if (button.textContent.trim() === "Show More") {
          // Show all hidden features with animation
          allHiddenFeatures.forEach((feature) => {
            feature.classList.remove("hidden"); // Remove the hidden class
            feature.style.maxHeight = feature.scrollHeight + "px"; // Expand height to show content
            feature.style.transition = "max-height 0.5s ease"; // Smooth transition
          });

          // Smooth scroll to the full card
          button.closest(".pricing-card").scrollIntoView({
            behavior: "smooth",
            block: "center"
          });

          button.textContent = "Show Less"; // Change the button text
        } else {
          // Hide extra features with animation
          allHiddenFeatures.forEach((feature) => {
            feature.style.maxHeight = "0"; // Collapse height
            feature.style.transition = "max-height 0.5s ease"; // Smooth transition
            feature.classList.add("hidden"); // Add hidden class back
          });

          // Add hidden class to all hidden features in all plans
          allCards.forEach((card) => {
            const hiddenFeatures = card.querySelectorAll(".hidden");
            hiddenFeatures.forEach((feature) => {
              feature.classList.add("hidden");
            });
          });

          button.textContent = "Show More"; // Change the button text back
        }
      });
    });
  });
</script>
